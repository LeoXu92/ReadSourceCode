/*
 @{
 @"name" : @"bang",
 @"phone": @{@"mobile": @"xx", @"home": @"xx"},
 @"families": @[@"father", @{@"key":@"mother"}],
 @"nums": [NSSet setWithObjects:@"1", @"2", nil]
 }
 ->
